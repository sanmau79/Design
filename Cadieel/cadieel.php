<h2>Comisiones Temáticas:</h2>
<strong>Comisión de Capacitación:</strong><br /> 
<p>Trabaja en la formulación de estrategias de capacitación para las empresas asociadas y vinculadas al sector. Entiende en temas vinculados a la formación empresarial y profesional en todos los estamentos de la empresa. Articula con el sistema educativo, académico y científico-tecnológico y tiene como objetivo agregar valor a la actividad de la cámara.</p>
