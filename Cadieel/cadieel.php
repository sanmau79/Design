<h1><a name="4" id="4"></a>Comisiones</h1>
CADIEEL desarrolla sus actividades a través de comisiones operativas integradas por sus socios. Cada una de ellas es presidida por un directivo de la entidad quien es, además, miembro del Comité de Presidencia y son coordinadas por un integrante del staff de la cámara.<br />
De este modo, se busca establecer la agenda y las prioridades según las necesidades de los asociados e implementar acciones dirigidas al logro de los objetivos propuestos.
 
<br /><br /><br />


<h2>Comisiones Sectoriales:</h2>



<strong>Comisión de Baja Tensión:</strong> <br />
Genera acciones orientadas a proteger los intereses de los fabricantes, especialmente en relación al cumplimiento de los fundamentos de la Resolución N° 92/98 y sus complementarias, que determinan los requisitos esenciales de seguridad eléctrica que debe cumplir el equipamiento eléctrico de baja tensión para su comercialización.<br />
Propicia un plan de acción por etapas tendiente a la readecuación de las instalaciones eléctricas existentes anteriores a 1995. Trabaja junto a secretarías del Estado Nacional, certificadoras, laboratorios, entes reguladores y de reglamentación.
