<h1><a name="1" id="1"></a>La Cámara</h1>
<p>  CADIEEL es una sociedad civil, sin fines de lucro que congrega a las empresas industriales cuyas actividades se desarrollan en el territorio de la Republica Argentina y que se relacionan con la producción de bienes que contengan elementos o partes mecánicas, eléctricas o electrónicas; de elementos o partes para software; productoras e integradoras de hardware o sistemas de software para plantas o redes que hagan uso de los rubros anteriores como así también de los servicios asociados. 
</p>
<p>CADIEEL se conformó el 3 de mayo de 2004 por la fusión de la <strong>Cámara Argentina de Industrias Electrónicas (CADIE)</strong> y la <strong>Cámara Argentina de Industrias Electromecánicas (CADIEM)</strong>, entidades que se sumaron para defender los intereses de los sectores involucrados y crear un sector electro-electrónico con peso propio en la industria argentina. En 2008, se produjo la fusión con la <strong>Cámara Argentina de Telecomunicaciones, Informática, Control Automático y Contenidos (CATYA)</strong>.</p>

<p>CADIEEL representa y defiende los intereses de más de 3.000 empresas de la industria electro-electrónica que emplean a más treinta mil personas y facturan anualmente más de 3.000 millones de dólares.
</p>
<p>Creamos soluciones que sirven para la mejora de la calidad de vida de las personas. Las industrias representadas por CADIEEL son los principales motores del progreso tecnológico del país. Nuestros productos constituyen tecnologías transversales que determinan el ritmo de innovación y de crecimiento de todas las ramas de la economía.
</p>
