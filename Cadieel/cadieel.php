<strong>Comisión de Energía:</strong> <br />
Participan, principalmente, empresas relacionadas con la generación, transmisión y distribución de energía eléctrica, fijando propuestas con el objeto de observar la problemática de los Sectores involucrados y buscar sus posibles soluciones. Es uno de sus objetivos crear el Centro Electrotécnico de Investigación y Ensayos (CEDIESA), un proyecto de convenio para impulsar la realización de un laboratorio de potencia.<br /><br />

