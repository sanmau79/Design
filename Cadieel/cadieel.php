<strong>Comisión de Comercio Exterior:</strong><br />
Asesora y trabaja, entre otros temas, sobre legislación nacional vigente aplicable a exportaciones, importaciones; aranceles; valoración aduanera; prácticas desleales; dumping; reintegros; regímenes especiales y clasificación arancelaria. <br />
Organiza acciones de promoción comercial como participación en ferias, misiones al exterior y rondas de negocios.
