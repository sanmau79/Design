<h1><a name="5" id="5"></a>Misión & Valores</h1>
<ul><h6>Misión:</h6>
<li>Ayudar al desarrollo competitivo de una industria nacional tecnológicamente actualizada, con sentido de solidaridad empresarial y responsabilidad social.</li></ul>




<br />
<ul><h6>Visión:</h6> 
<li>Ser el organismo de máxima representatividad de los emprendimientos industriales del sector que promueva responsablemente el desarrollo industrial en un entorno local, regional y global, con servicios de alta tecnología y calidad.</li></ul>




<br />
<ul><h6>Valores:</h6>
<li>Representación de la actividad industrial del sector en todo el país.</li>
<li>Igualdad y ecuanimidad en la atención a los problemas presentados por sus socios. </li>
<li>Operar en forma proactiva con todos los sectores que tengan como finalidad el correcto y próspero crecimiento empresarial.</li> 

<li>Voluntad e ímpetu para crear una industria sólida, respetuosa del medio ambiente, asegurando el  apoyo oficial y de los entes afines con la actividad.</li> 
<li>Eficacia en la gestión operativa y administrativa  de la institución.</li></ul>




<br />
<ul><h6>Objetivos:</h6>
<li>Dar presencia institucional a CADIEEL y a sus asociados en todos los ámbitos directa o indirectamente ligados al sector.</li>

<li>Promover el ingreso en nuevos mercados y la concreción de negocios para los asociados, mediante la presentación de  las empresas y sus productos en el contexto nacional, regional e internacional.</li>

<li>Consolidar, desarrollar e impulsar la competitividad de los sectores que representamos.</li>


<li>Gestionar todas aquellas actividades que fortalezcan la industria; generar empleo principalmente  técnico y especializado;  trabajar con una visión gremial y con el compromiso de llevar los beneficios de la tecnología a todos los rincones del país.</li>

<li>Representar y defender los intereses de los asociados ante el sector público y las autoridades nacionales, provinciales y municipales.</li>


<li>Divulgar y propender a la adopción de principios universales de calidad para productos y procesos y las aplicables al cuidado del medio ambiente.</li>

<li>Generar espacios de cooperación e intercambio entre empresas del sector y de otras instituciones y entidades.</li>

<li>Promover la creación de nuevas empresas y la inversión para el desarrollo y ampliación de las ya instaladas.</li></ul>



<br />
<ul>
  <h6>Metas:</h6>
  <li>CADIEEL tiene como meta la promoción del desarrollo de la industria electro-electrónica como sector estratégico para el país. Además, busca contribuir a la difusión y el cumplimiento de las leyes que regulan el  Compre Nacional y promociona el valor de la seguridad eléctrica y la adecuación de las instalaciones eléctricas para la protección de las personas y sus bienes.</li>
<li>Como reflejo del estado del sector, CADIEEL trabaja por el fortalecimiento y desarrollo de la BIEL light+building para que sea el evento del sector de mayor importancia en América Latina.</li></ul>
