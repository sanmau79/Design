 <strong>Comisión de Electrónica:</strong> <br />
Se aboca a la defensa ante organismos estatales y privados de la industria electrónica instalada, a estudiar sus problemas y a tratar de hallar soluciones contribuyendo al desarrollo del sector.<br />
Además, realiza un exhaustivo seguimiento de la normativa pertinente y de los planes de inversión de las empresas de servicio público de telecomunicaciones.<br />
Entre otras actividades, promueve una nueva Ley de Telecomunicaciones acorde al uso de las nuevas tecnologías. Reclama la correcta utilización del espectro radioeléctrico y la homologación de equipos de telecomunicaciones.
