<strong>Comisión de Iluminación:</strong> <br />
Está interesada en la defensa del mercado de elementos de iluminación: luminarias, equipos auxiliares y lámparas, contra la agresión de alternativas de baja calidad -nacional o importada. Le es de suma importancia la aplicación racional de la Resolución 92/98 y dialoga para este fin con las autoridades de aplicación a través de CADIEEL.<br />
Para el uso racional y eficiente de la energía eléctrica elabora recomendaciones de uso e interactúa con reparticiones públicas y privadas para su difusión. Actúa proactivamente en la concientización de los distribuidores de materiales de iluminación respecto a la legalidad de los productos que se comercializan.
